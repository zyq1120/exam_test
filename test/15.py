"""
15. 编写程序，设计一个字典，用户输入内容作为“键”，然后输出字典对应的“值”，若键不存在，则输出提示信息“键不存在”
【问题描述】编写程序，某字典{"name":"Zhangsan","sex":"M","address":"Nanjing","phone":"123456"}，用户输入内容作为“键”，若键不存在，则输出提示信息“0”，若键存在，则输出提示信息“1”

【输入形式】用户输入内容作为“键”

【输出形式】1代表“键存在”，0代表“键不存在”
【样例输入】A

【样例输出】0
"""
d = {"name":"Zhangsan","sex":"M","address":"Nanjing","phone":"123456"}

n = input()
flag = 0
for i in d.keys():

    if i == n:
        flag = 1
print(flag)
