"""
7. 求球的运动路程和反弹高度
【问题描述】

球从100米高落下，每次落地后反弹高度为原来的一半，求它在第十次落地时，共经过多少米？

第十次反弹高度是多少？（结果保留2位小数）


【输入形式】
【输出形式】
【样例输入】
【样例输出】总路程:xxx.xx,第十次高度:x.xx
"""

w = 100
n = w / 2

for i in range(2,11):
    w = n + w
    n = n / 2
print("总路程:{:.2f},第十次高度:{:.2f}".format(w,n))




s = 100
x = s/2

for i in range(2,11):
    s = s+2*x
    x = x/2
print("总路程:{:.2f},第十次高度:{:.2f}".format(s,x))