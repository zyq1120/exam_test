"""

编程统计s中汉字和标点符号的个数

【问题描述】字符串s中保存了论语中的一句话，请编程统计s中汉字和标点符号的个数。s="学而时习之,不亦说乎?有朋自远方来,不亦乐乎?人不知而不愠,不亦君子乎?"

【样例输入】s="学而时习之,不亦说乎?有朋自远方来,不亦乐乎?人不知而不愠,不亦君子乎?"
【样例输出】汉字个数为30,标点符号个数6。
"""


s = "学而时习之,不亦说乎?有朋自远方来,不亦乐乎?人不知而不愠,不亦君子乎?"
punctuation = s.count(',')+s.count('?')
word  =  len(s) - punctuation
print("汉字个数为{},标点符号个数{}".format(word,punctuation))

